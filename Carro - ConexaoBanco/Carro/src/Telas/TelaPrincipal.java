    private void jlbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jlbCadastrarActionPerformed
        // TODO add your handling code here:
        DescricaoDAO descricaoDAO = new DescricaoDAO();
        String desc = jtfDescricao.getText().trim();
        String pot = jtfPotencia.getText().trim();
        String cor = jtfCor.getText().trim();
    
    if (!desc.isEmpty() && !pot.isEmpty() && !cor.isEmpty()) {
        Descricao descricao = descricaoDAO.adicionar(desc, pot, cor);
        if (descricao != null) {
            JOptionPane.showMessageDialog(null, "Descrição adicionada com sucesso.");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao adicionar a descrição.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.");
    }

    }//GEN-LAST:event_jlbCadastrarActionPerformed

