    private void jlbListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jlbListarActionPerformed
        // TODO add your handling code here:
          DefaultTableModel model = (DefaultTableModel) jtbDescricaoT.getModel();
        model.setRowCount(0);
        
        DescricaoDAO cadcarros = new DescricaoDAO();
        ArrayList<Descricao> listacadcarros = new ArrayList<>();
        listacadcarros = cadcarros.getDescricoes();
        for (Descricao descricao: listacadcarros){
            Object [] linha = {
            descricao.getCod(),
            descricao.getDesc(),
            descricao.getPot(),
            descricao.getCor()
         };
         model.addRow(linha);
         }
              
    }//GEN-LAST:event_jlbListarActionPerformed

